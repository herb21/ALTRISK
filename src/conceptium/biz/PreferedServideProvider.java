    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String courseID = cboCourseID.getSelectedItem().toString();
        String courseName = txtCourseName.getText();
        String provider = txtServiceProvider.getText();
        String site = txtLocation.getText();
        String status = cboStatus.getSelectedItem().toString();
        String preferedProvider = "Prefered Service Provider";
        String sql = "select * from PreferedProvider where CourseID = ? and Status = ?";
        String sql1 = "Insert into PreferedProvider(CourseID, CourseName,Provider,Status,Location)values (?, ?, ?,?,?)";
        try{
            Connection con = DriverManager.getConnection("jdbc:derby:MTD", "herbert", "elsie1*#");
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, courseID);
            pst.setString(2, preferedProvider);
            ResultSet rs = pst.executeQuery();
            if(rs.wasNull()){
                try{
                    pst = con.prepareStatement(sql1);
                    pst.setString(1, courseID);
                    pst.setString(2, courseName);
                    pst.setString(3, provider);
                    pst.setString(4, status);
                    pst.setString(5, site);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(PreferedServideProvider.this, provider +" "+ "successfuly saved as a prefered Provider for" + " "+courseName);
                }
                catch(SQLException | HeadlessException e){
                    JOptionPane.showMessageDialog(PreferedServideProvider.this, e);
                }
            }
            else{
                JOptionPane.showMessageDialog(PreferedServideProvider.this, "Default provider already set");
            }
        }catch(SQLException | HeadlessException ex){
            JOptionPane.showMessageDialog(PreferedServideProvider.this, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

